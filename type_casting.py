#Type Casting: the ability to convert a datatype to another datatype

x, y, z = 10, 20.45, "30"

print(x)
print(y)
print(z)

#casting to integer
y = int(y)
z = int(z)
print(y)
print(z)

#casting to float
x = float(x)
z = float(z)
print(x)
print(z)

#casting to string
x = str(x)
y = str(y)
print(x)
print(y)